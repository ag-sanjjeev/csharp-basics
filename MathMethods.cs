// Math methods

using System;


class MathMethods {


	static void Main(string[] args) {
    
    
    	Console.WriteLine(Math.Max(5, 10));
        Console.WriteLine(Math.Min(10, 5));
        Console.WriteLine(Math.Sqrt(25));
        Console.WriteLine(Math.Abs(-14));
        Console.WriteLine(Math.Round(12.2)); // like ceil function
        Console.WriteLine(Math.Floor(11.2));
        Console.WriteLine(Math.Tan(30));
        Console.WriteLine(Math.Log10(2));
    
    }

}
